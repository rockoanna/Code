#include <string>
#include <iostream>

using namespace std;

int main()
{
    int n;
    cin >> n;
    for(int i = 0; i < n; i++){
        string nr;
        cin >> nr;
        if(nr.size() == 5){
            cout << 3 << endl;
        }else{
            if(nr[0] == 'o' || nr[1] == 'n'){
                cout << 1 << endl;
            }else{
                cout << 2 << endl;
            }
        }

    }

    return 0;
}
